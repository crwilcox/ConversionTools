using System;
using Alteridem.GitHub.Annotations;
using Octokit;

namespace Alteridem.GitHub.Model
{
    /// <summary>
    /// Make it so that we can find Repositories in collections and we can sort them by name.
    /// </summary>
    public class RepositoryWrapper : IEquatable<RepositoryWrapper>, IComparable<RepositoryWrapper>
    {
        public Repository Repository { get; set; }
        
        public RepositoryWrapper([NotNull] Repository repository)
        {
            Repository = repository;
        }

        /// <summary>
        /// Indicates whether the current object is equal to another object of the same type.
        /// </summary>
        /// <returns>
        /// true if the current object is equal to the <paramref name="other"/> parameter; otherwise, false.
        /// </returns>
        /// <param name="other">An object to compare with this object.</param>
        public bool Equals(RepositoryWrapper other)
        {
            return Repository.Id == other.Repository.Id;
        }

        /// <summary>
        /// Compares the current object with another object of the same type.
        /// </summary>
        /// <returns>
        /// A value that indicates the relative order of the objects being compared. The return value has the following meanings: Value Meaning Less than zero This object is less than the <paramref name="other"/> parameter.Zero This object is equal to <paramref name="other"/>. Greater than zero This object is greater than <paramref name="other"/>. 
        /// </returns>
        /// <param name="other">An object to compare with this object.</param>
        public int CompareTo(RepositoryWrapper other)
        {
            return Repository.FullName.CompareTo(other.Repository.FullName);
        }

        /// <summary>
        /// Determines whether the specified <see cref="T:System.Object"/> is equal to the current <see cref="T:System.Object"/>.
        /// </summary>
        /// <returns>
        /// true if the specified object  is equal to the current object; otherwise, false.
        /// </returns>
        /// <param name="obj">The object to compare with the current object. </param>
        public override bool Equals(object obj)
        {
            if (obj is RepositoryWrapper)
                return Equals((RepositoryWrapper)obj);

            return false;
        }

        /// <summary>
        /// Serves as a hash function for a particular type. 
        /// </summary>
        /// <returns>
        /// A hash code for the current <see cref="T:System.Object"/>.
        /// </returns>
        public override int GetHashCode()
        {
            return Repository.Id.GetHashCode();
        }

        /// <summary>
        /// Returns a string that represents the current object.
        /// </summary>
        /// <returns>
        /// A string that represents the current object.
        /// </returns>
        public override string ToString()
        {
            return Repository.FullName;
        }
    }
}